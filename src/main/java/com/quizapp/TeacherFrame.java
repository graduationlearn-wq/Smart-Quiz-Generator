    private void loadQuizzes() {
        cmbQuizzes.removeAllItems();
        try (java.sql.Connection conn = DBConnection.getConnection()) {
            String query = "SELECT quiz_id, title FROM quizzes WHERE teacher_id = ? ORDER BY created_at DESC";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, SessionManager.getInstance().getUserId());
            java.sql.ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                cmbQuizzes.addItem(rs.getInt("quiz_id") + " - " + rs.getString("title"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading quizzes: " + e.getMessage());
        }
    }

    private void loadQuestions() {
        if (cmbQuizzes.getSelectedItem() == null) return;
        String selected = cmbQuizzes.getSelectedItem().toString();
        if (!selected.contains(" - ")) return;

        int quizId = Integer.parseInt(selected.split(" - ")[0]);

        try (java.sql.Connection conn = DBConnection.getConnection()) {
            String query = "SELECT question_id, question_text, option_a, option_b, option_c, option_d, correct_option FROM questions WHERE quiz_id = ?";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, quizId);
            java.sql.ResultSet rs = pst.executeQuery();

            javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(
                new String[]{"ID", "Question", "A", "B", "C", "D", "Correct"}, 0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("question_id"),
                    rs.getString("question_text"),
                    rs.getString("option_a"),
                    rs.getString("option_b"),
                    rs.getString("option_c"),
                    rs.getString("option_d"),
                    rs.getString("correct_option")
                });
            }
            tblQuestions.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error loading questions: " + e.getMessage());
        }
    }
