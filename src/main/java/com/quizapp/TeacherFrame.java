    private void btnCreateQuizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateQuizActionPerformed
        String title = txtQuizTitle.getText().trim();

        if (!Validator.isValidQuizTitle(title)) {
            JOptionPane.showMessageDialog(this, "Quiz title must be 3-255 characters!");
            return;
        }

        try (java.sql.Connection conn = DBConnection.getConnection()) {
            String query = "INSERT INTO quizzes (teacher_id, title) VALUES (?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, SessionManager.getInstance().getUserId());
            pst.setString(2, title);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Quiz created successfully!");
            txtQuizTitle.setText("");
            loadQuizzes();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnCreateQuizActionPerformed

    private void btnSaveQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveQuestionActionPerformed
        if (cmbQuizzes.getSelectedItem() == null || !cmbQuizzes.getSelectedItem().toString().contains(" - ")) {
            JOptionPane.showMessageDialog(this, "Please select a quiz first!");
            return;
        }

        String selected = cmbQuizzes.getSelectedItem().toString();
        int quizId = Integer.parseInt(selected.split(" - ")[0]);

        String question = txtQuestion.getText().trim();
        String optA = txtOptA.getText().trim();
        String optB = txtOptB.getText().trim();
        String optC = txtOptC.getText().trim();
        String optD = txtOptD.getText().trim();
        String correct = cmbCorrectOption.getSelectedItem().toString();

        if (!Validator.isValidQuestion(question) ||
            !Validator.isValidOption(optA) ||
            !Validator.isValidOption(optB) ||
            !Validator.isValidOption(optC) ||
            !Validator.isValidOption(optD)) {
            JOptionPane.showMessageDialog(this, "Please fill all fields correctly!");
            return;
        }

        try (java.sql.Connection conn = DBConnection.getConnection()) {
            String query = "INSERT INTO questions (quiz_id, question_text, option_a, option_b, option_c, option_d, correct_option) VALUES (?, ?, ?, ?, ?, ?, ?)";
            java.sql.PreparedStatement pst = conn.prepareStatement(query);
            pst.setInt(1, quizId);
            pst.setString(2, question);
            pst.setString(3, optA);
            pst.setString(4, optB);
            pst.setString(5, optC);
            pst.setString(6, optD);
            pst.setString(7, correct);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Question saved!");
            txtQuestion.setText("");
            txtOptA.setText("");
            txtOptB.setText("");
            txtOptC.setText("");
            txtOptD.setText("");
            cmbCorrectOption.setSelectedIndex(0);
            loadQuestions();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveQuestionActionPerformed

